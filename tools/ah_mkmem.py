#!/usr/bin/env python3
# Generate ah_mem.v: a unified dual-port physical RAM (fetch + data share one
# array), preloaded with a full program image (.text + .rodata + .data).
# (c) 2026 Ali Hussein, GPL-3.0.  usage: ah_mkmem.py out.v image.bin [WORDS]
import sys, struct
img = open(sys.argv[2],'rb').read()
WORDS = int(sys.argv[3]) if len(sys.argv) > 3 else 32768   # 256 KB default
if len(img) % 8: img += b'\x00'*(8-len(img)%8)
words = [struct.unpack('<Q', img[i:i+8])[0] for i in range(0,len(img),8)]
AW = (WORDS-1).bit_length()
L = []
L.append('// ============================================================================')
L.append('//  AH-RISCV : unified dual-port physical RAM. (c) 2026 Ali Hussein. GPL-3.0.')
L.append('//  Port A = instruction fetch (read 32-bit). Port B = data (r/w, sized).')
L.append('//  Both index one array, so code and data share one physical address space')
L.append('//  (RAM base 0x8000_0000). Generated by tools/ah_mkmem.py.')
L.append('// ============================================================================')
L.append('module ah_mem (')
L.append('    input         clk,')
L.append('    input  [63:0] a_addr,  output reg [31:0] a_rdata,         // fetch port')
L.append('    input  [63:0] b_addr,  input [63:0] b_wdata, input [2:0] b_funct3,')
L.append('    input         b_re,    input b_we,  output reg [63:0] b_rdata  // data port')
L.append(');')
L.append(f'    localparam WORDS={WORDS};')
L.append(f'    reg [63:0] mem [0:WORDS-1];')
L.append('    integer i;')
L.append('    initial begin')
L.append('        for (i=0;i<WORDS;i=i+1) mem[i]=64\'d0;')
for i,w in enumerate(words):
    if w: L.append(f"        mem[{i}]=64'h{w:016x};")
L.append('    end')
L.append(f'    wire [{AW-1}:0] a_idx = a_addr[{AW+2}:3];')
L.append(f'    wire [{AW-1}:0] b_idx = b_addr[{AW+2}:3];')
L.append('    // ---- port A: instruction fetch (registered 32-bit read) ----')
L.append('    always @(posedge clk) a_rdata <= a_addr[2] ? mem[a_idx][63:32] : mem[a_idx][31:0];')
L.append('    // ---- port B: data, byte/half/word/double store + sized read ----')
L.append('    wire [5:0] boff = {b_addr[2:0],3\'b000};')
L.append('    always @(posedge clk) if (b_we) begin')
L.append('        case (b_funct3[1:0])')
L.append('            2\'b00: mem[b_idx][boff +:  8] <= b_wdata[7:0];')
L.append('            2\'b01: mem[b_idx][boff +: 16] <= b_wdata[15:0];')
L.append('            2\'b10: mem[b_idx][boff +: 32] <= b_wdata[31:0];')
L.append('            2\'b11: mem[b_idx]             <= b_wdata;')
L.append('        endcase')
L.append('    end')
L.append('    reg [63:0] word_r; reg [5:0] boff_r; reg [2:0] f3_r;')
L.append('    always @(posedge clk) begin word_r<=mem[b_idx]; boff_r<=boff; f3_r<=b_funct3; end')
L.append('    wire [63:0] sub = word_r >> boff_r;')
L.append('    always @(*) begin')
L.append('        case (f3_r)')
L.append('            3\'b000: b_rdata = {{56{sub[7]}},  sub[7:0]};')
L.append('            3\'b001: b_rdata = {{48{sub[15]}}, sub[15:0]};')
L.append('            3\'b010: b_rdata = {{32{sub[31]}}, sub[31:0]};')
L.append('            3\'b011: b_rdata = word_r;')
L.append('            3\'b100: b_rdata = {56\'b0, sub[7:0]};')
L.append('            3\'b101: b_rdata = {48\'b0, sub[15:0]};')
L.append('            3\'b110: b_rdata = {32\'b0, sub[31:0]};')
L.append('            default: b_rdata = word_r;')
L.append('        endcase')
L.append('    end')
L.append('endmodule')
L.append('')
open(sys.argv[1],'w').write("\n".join(L))
print(f"{sys.argv[1]}: {WORDS} words ({WORDS*8//1024} KB), {len(words)} image words preloaded")
